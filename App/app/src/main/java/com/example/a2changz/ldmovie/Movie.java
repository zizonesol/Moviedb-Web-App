class Movie {

    String title;
    String year;
    String director;
    String allstar;
    String genre;

    public String getTitle()
    {
        return title;
    }

    public String getYear(){
        return year;
    }

    public String getDirector(){
        return director;
    }

    public String getAllstar()
    {
        return allstar;
    }
    public String getGenre(){
        return genre;
    }
}
